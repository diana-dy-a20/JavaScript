//Задача №1
// Дано ширину екрана та кількість елементів, які повинні бути відображені у рядку. 
// Визначити ширину елементів, які повинні бути відображені у цьому рядку. 

//Задаємо значення

const screenWidth = parseInt(prompt('Введіть ширину екрану в пікселях', 1440))
const items = parseInt(prompt('Введіть кількість елементів', 4))

//Обчислюємо 

let itemWidth = screenWidth / items

// Виводимо результат

document.write(`Ширина елементу - ${itemWidth} px.`)
